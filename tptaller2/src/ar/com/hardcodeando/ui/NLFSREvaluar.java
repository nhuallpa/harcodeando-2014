    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        //validate step 2
        try{
            seed = Integer.parseInt(jTextField1.getText());
            if(!Integer.toBinaryString(seed).equals(jTextField2.getText())){
                showPopup(jPanel1, "Registro de bits inválido.");
            }else{
                showPopup(jPanel1, "Semilla definida correctamente. Puede continuar con el próximo paso.");
                jButton1.setEnabled(true);
                length = Integer.toBinaryString(seed).length();
                seedStr = Integer.toBinaryString(seed);
            }
        }
        catch(NumberFormatException e){
            showPopup(jPanel1, "Semilla inválida.");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

