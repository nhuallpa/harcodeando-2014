    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        //validate step 3
        try{
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            String k = (String)model.getValueAt(0, 1);
            int x = Integer.parseInt(model.getValueAt(0, 2).toString());
            
            String message = new String();
            if(String.valueOf(NLFSRCommon.getKey(seedStr)) == null ? k != null : !String.valueOf(NLFSRCommon.getKey(seedStr)).equals(k)){
                message = message + "El bit clave es incorrecto. ";
            }
            if(x != function.getResult(nlfsr.getSeedArray(seedStr))){
                message = message + "El x calculado es incorrecto.";
            }
            
            if(message.isEmpty()){
                showPopup(jPanel3, "Bit clave y x correctos. Puede continuar con el proximo paso.");
                jButton6.setEnabled(true);
                s0 = x;
                key = k;
            }
            else{
                showPopup(jPanel3, message);
            }
            
        }catch(Exception e){
            showPopup(jPanel3, "El x calculado es incorrecto.");
        }
        
    }//GEN-LAST:event_jButton7ActionPerformed

